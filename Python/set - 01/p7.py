# Check various arithmetic operators of Python
a = float(input("Enter the value for 'a': "))
b = float(input("Enter the value for 'b': "))

addition_result = a + b
print("\nAddition: {a} + {b} = {addition_result}")

subtraction_result = a - b
print("\nSubtraction: {a} - {b} = {subtraction_result}")

multiplication_result = a * b
print("\nMultiplication: {a} * {b} = {multiplication_result}")

division_result = a / b
print("\nDivision: {a} / {b} = {division_result}")

floor_division_result = a // b
print("\nFloor Division: {a} // {b} = {floor_division_result}")

modulus_result = a % b
print("\nModulus: {a} % {b} = {modulus_result}")

exponentiation_result = a ** b
print("\nExponentiation: {a} ** {b} = {exponentiation_result}")
